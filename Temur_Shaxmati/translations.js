// ── Aktiv til va t() funksiyasi ──────────────────────────────
let currentLang = localStorage.getItem("atc_lang") || "uz";

function setLang(lang) {
  if (TRANSLATIONS[lang]) {
    currentLang = lang;
    localStorage.setItem("atc_lang", lang);
    document.dispatchEvent(new Event("langChanged"));
  }
}

function t(key) {
  const lang = TRANSLATIONS[currentLang] || TRANSLATIONS["uz"];
  return lang[key] !== undefined ? lang[key] : (TRANSLATIONS["uz"][key] || key);
}

function tStep(index) {
  const lang = TRANSLATIONS[currentLang] || TRANSLATIONS["uz"];
  return lang.tut_steps && lang.tut_steps[index]
    ? lang.tut_steps[index]
    : TRANSLATIONS["uz"].tut_steps[index];
}

function getAvailableLangs() {
  return Object.keys(TRANSLATIONS).map(code => ({
    code,
    name: TRANSLATIONS[code].langName
  }));
}